package com.mechanoid.net;

public class Item {
	private String a;
	private int b;
	private long c;
	private double d;
	private boolean e;
	
	public String getA(){
		return a;
	}
	public void setA(String value){
		this.a = value;
	}
	public int getB(){
		return b;
	}
	public void setB(int value){
		this.b = value;
	}
	public long getC(){
		return c;
	}
	public void setC(long value){
		this.c = value;
	}
	public double getD(){
		return d;
	}
	public void setD(double value){
		this.d = value;
	}
	public boolean isE(){
		return e;
	}
	public void setE(boolean value){
		this.e = value;
	}
}
