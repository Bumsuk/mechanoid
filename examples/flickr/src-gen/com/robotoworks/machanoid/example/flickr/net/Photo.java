package com.robotoworks.machanoid.example.flickr.net;

public class Photo {
	private int id;
	private int owner;
	
	public int getId(){
		return id;
	}
	public void setId(int value){
		this.id = value;
	}
	public int getOwner(){
		return owner;
	}
	public void setOwner(int value){
		this.owner = value;
	}
}
